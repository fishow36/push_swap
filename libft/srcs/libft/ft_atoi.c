#include "libft.h"

int		ft_atoi(const char *nptr)
{
	int sign;
	int result;

	sign = 1;
	while (*nptr == ' ' || *nptr == '\n' || *nptr == '\t' ||
		*nptr == '\r' || *nptr == '\v' || *nptr == '\f')
		nptr++;
	if (*nptr == '-')
	{
		sign = -1;
		nptr++;
	}
	else if (*nptr == '+')
		nptr++;
	result = 0;
	while (*nptr >= '0' && *nptr <= '9')
	{
		result = result * 10 + *nptr - '0';
		nptr++;
	}
	return (result * sign);
}
